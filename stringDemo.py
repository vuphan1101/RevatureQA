#create a python file, create a string as " Hello World ".
#String: sequence of character, immutable
#Use the common string method like
#lower, upper, strip(), lstrip, rstrip, replace, split, join, find, index

text = " Hello World "
print(text.upper())
print(text.lower())
print(text.lstrip())
print(text.rstrip())
print(text.replace('Hello', 'Hi'))
print(text.split())
print(text.join(['Plz', 'Hi']))
print(text.find('World'))
print(text.index('H'))